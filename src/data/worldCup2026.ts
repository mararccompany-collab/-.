// توقعات الفرق حسب القوة
export function getTeamPredictions(team: Team) {
  const strength = team.strength;
  return {
    toAdvance: Math.min(85 + (strength - 65) * 0.5, 95),
    toTopGroup: Math.min(50 + (strength - 65) * 0.8, 85),
    toSemiFinal: Math.min(20 + (strength - 65) * 0.4, 75),
    toFinal: Math.min(5 + (strength - 65) * 0.15, 40),
    toWin: Math.max(strength - 55, 1),
  };
}

// احصائيات الفرق
export function getTeamStats(teamId: string) {
  const team = Object.values(teamsDatabase).find(t => t.id === teamId);
  if (!team) return null;
  
  return {
    team,
    statistics: {
      fifaRank: team.fifaRank,
      worldCupParticipations: team.worldCupParticipations,
      titles: team.titles,
      strength: team.strength,
      continent: team.continent,
    },
    predictions: getTeamPredictions(team),
  };
}
